const deleteEltList = document.getElementsByClassName('trash');

// /***********************************************************************************/
/* ********************************** FONCTIONS ************************************/
/***********************************************************************************/




/************************************************************************************/
/* ******************************** CODE PRINCIPAL **********************************/
/************************************************************************************/
document.addEventListener('DOMContentLoaded', () => {
    for (let i = 0; i < deleteEltList.length; i++) {
        deleteEltList[i].addEventListener("click", function() {
            return confirm("Es-tu sûre de vouloir supprimer cet élément ?");
        });
    }
}) 